/*
A pointer is a variable that stores the memory address of another variable.

Instead of holding a value directly, it “points” to the location where the 
value is stored.

Why are pointers useful?
- They allow direct access and manipulation of memory.
- Enable dynamic memory management.
- Facilitate efficient passing of large data structures to functions without copying.
- Essential for working with arrays, strings, and complex data structures.

How to declare a pointer?
You declare a pointer by specifying the type of data it points to, followed 
by an asterisk (*), then the pointer name.

Syntax:

    type *pointer_name;

Example:

    int *ptr;   // ptr is a pointer to an int
    char *cptr; // cptr is a pointer to a char
*/
